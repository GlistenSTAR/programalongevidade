      <p id="u64831-2">AO CLICAR NO BOTÃO “ACEITO”, POR OCASIÃO DO CADASTRO VIA INTERNET, O USUÁRIO ADERE E CONCORDA AUTOMÁTICA E INTEGRALMENTE COM OS TERMOS E CONDIÇÕES DESTE REGULAMENTO.</p>
      <p id="u64831-3">&nbsp;</p>
      <p id="u64831-5">1. O presente documento regula o cadastro e participação do usuário (“USUÁRIO”) no Programa de Relacionamento da TORRENT PHARMA denominado Programa Longevidade (“Programa”).</p>
      <p id="u64831-6">&nbsp;</p>
      <p id="u64831-8">2. O Programa disponibiliza dicas de saúde e informações úteis ao tratamento dos pacientes cadastrados. O Programa, por meio da concessão de descontos na compra de determinados medicamentos da TORRENT em farmácias, objetiva ainda contribuir para a adesão dos pacientes ao tratamento prescrito com o fim de reduzir a incidência de abandono do tratamento por motivos econômicos.</p>
      <p id="u64831-9">&nbsp;</p>
      <p id="u64831-11">3. É condição para participação no Programa que o médico tenha prescrito ao USUÁRIO algum dos medicamentos que fazem parte do Programa. Para cadastrar-se, o USUÁRIO deverá informar seu nome, endereço, CPF, bem como o número CRM do médico prescritor, quando necessário. O cadastro deverá ser feito pelo próprio paciente que recebeu a prescrição médica ou por seu representante legal. O direito de usufruir do Programa é restrito ao USUÁRIO cadastrado e não pode ser transferido a terceiros. Após o cadastro, o USUÁRIO poderá fazer a compra do medicamento em qualquer farmácia participante, identificando-se por meio do seu número de CPF.</p>
      <p id="u64831-12">&nbsp;</p>
      <p id="u64831-14">4. As informações fornecidas pelo USUÁRIO no momento do cadastro são de responsabilidade exclusiva dos mesmos e serão consideradas pela TORRENT PHARMA como exatas e verdadeiras, responsabilizando-se o USUÁRIO integralmente por sua exatidão e veracidade.</p>
      <p id="u64831-15">&nbsp;</p>
      <p id="u64831-17">5. Os dados pessoais informados para cadastro do USUÁRIO no Programa serão mantidos em ambiente seguro, a fim de garantir a sua integridade, serão tratados como dados confidenciais e não serão utilizados para qualquer fim que não o adequado funcionamento do Programa, conforme Política de Privacidade de dados da TORRENT PHARMA (http://www.torrent.com.br/pt/politica-de-privacidade.php). O cadastro do paciente no Programa é administrado por empresa terceirizada, vinculada a obrigações semelhantes de sigilo, confidencialidade e restrição de uso com relação a tais informações.</p>
      <p id="u64831-18">&nbsp;</p>
      <p id="u64831-20">6. A TORRNT PHARMA poderá usar os dados pessoais do USUÁRIO exclusivamente para fins de acompanhamento do Programa e envio de correspondência contendo documentos e informações pertinentes ao Programa. A TORRENT PHARMA poderá, ainda, se autorizado expressamente pelo USUÁRIO no momento do cadastramento, utilizar tais dados para envio de correspondência contendo informações gerais de saúde e qualidade de vida, bem como informações sobre outros produtos e serviços.</p>
      <p id="u64831-21">&nbsp;</p>
      <p id="u64831-23">7. O USUÁRIO poderá, a qualquer momento, solicitar o cancelamento do seu cadastro no Programa, ou revogar sua autorização para o envio de correspondências não diretamente relacionadas ao Programa, mediante contato pelo número 0800 333 1234.</p>
      <p id="u64831-24">&nbsp;</p>
      <p id="u64831-26">8. O USUÁRIO compromete-se a observar atentamente o período de tratamento indicado pelo médico na prescrição.</p>
      <p id="u64831-27">&nbsp;</p>
      <p id="u64831-29">9. A TORRENT PHARMA declara que não interfere na autonomia, independência e liberdade do profissional de saúde na prescrição da terapia adequada a seus pacientes e que nenhum tipo de incentivo, vantagem ou benefício será concedido ao médico prescritor pelo cadastramento de seu(s) paciente(s) no Programa.</p>
      <p id="u64831-30">&nbsp;</p>
      <p id="u64831-32">10. O cadastramento do paciente no Programa não representa concordância da TORRENT PHARMA com o diagnóstico médico.</p>
      <p id="u64831-33">&nbsp;</p>
      <p id="u64831-35">11. Fica eleito o foro central da Comarca da Capital do Estado de São Paulo, com renúncia a qualquer outro, por mais privilegiado que seja, para dirimir qualquer controvérsia derivada do presente Contrato.</p>
      <p id="u64831-36">&nbsp;</p>
      <p id="u64831-38">12. Os Produtos participantes e validade das campanhas estão indicados abaixo.</p>
      <p id="u64831-39">&nbsp;</p>
      <p id="u64831-41">13. A Torrent do Brasil reserva-se o direito de alterar ou interromper o Programa Longevidade, bem como incluir ou excluir medicamentos no Programa Longevidade, a qualquer momento, sem aviso prévio.</p>
